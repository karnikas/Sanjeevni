    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        if (validateInputs()==false)
        {
            JOptionPane.showMessageDialog(null, "Please enter all fields");
        }
        String validpassword=matchpassword();
        if(validpassword!= null)
        {
            JOptionPane.showMessageDialog(null, validpassword);
            return;
        }

        try
        {
            ReceptionistPojo recep = new ReceptionistPojo();     //set all details from the frame into doc pojo
           // recep.setReceptionist_id(jcrecepID.getSelectedItem().toString());
            recep.setReceptionist_id(selrecid);
            recep.setReceptionist_name(recname);
            recep.setGender(combogender.getSelectedItem().toString());

            boolean result1= ReceptionistDao.addRecep (recep); //storing results that whether the details are added in  tables or not
            if(result1==true )
            {
                JOptionPane.showMessageDialog(null, "Details added successfully in receptionist table");
                clearALL();
                return;
            }
            JOptionPane.showMessageDialog(null, "Updation failed");
            return;

        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in DB" +ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnupdateActionPerformed

