private void loadReceptionistId() {
        try{
            List<String>recepList=ReceptionistDao.getAllReceptionistId();
            for(String recId:recepList){
                jcrecepID.addItem(recId);
            } 
       }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            ex.printStackTrace();
        }
    }
private boolean validateInputs() {
      loginid=txtlogin.getText().trim();
      char[] p1=pwd1.getPassword();
      char[] p2=pwd2.getPassword();
      if(loginid.isEmpty() || p1.length==0 || p2.length==0)
      {
          return false;
      }
      pass1=new String(p1);
      pass2=new String(p2);
      return true;
    }

    private String matchpassword() {
      pass1= pass1.trim();
       pass2= pass2.trim();
       if(pass1.length()<3 || pass2.length()<3)
       {
         return "Passwords must be of atleast 3 characters in length";
       }
         if(pass1.equals(pass2)==false)
     {
       return "Passwords do not match";
     }
       return null;
    }

