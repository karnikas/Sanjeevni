    private void loadPatientDetails() {
      try
      {
          List<PatientsPojo> patList = PatientDao.getAllPatientDetails(); 
          if(patList.isEmpty())
          {
              JOptionPane.showMessageDialog(null, "no record found");
              return; 
          }
          for(PatientsPojo p: patList)
          {
          Vector <String> v = new Vector<>(); 
          v.add(String.valueOf(p.getPatient_id()));
          v.add(String.valueOf(p.getFirst_name()));
          v.add(String.valueOf(p.getLast_name()));
          v.add(String.valueOf(p.getAge()));
          v.add(String.valueOf(p.getOpd()));
          v.add(String.valueOf(p.getGender())); 
          v.add(String.valueOf(p.getM_status()));
          v.add(String.valueOf(p.getDate()));
          v.add(String.valueOf(p.getAddress())); 
          v.add(String.valueOf(p.getMob_no())); 
          v.add(String.valueOf(p.getDoc_id())); 
          
          model.addRow(v);
                System.out.println(v);                
            }
        }
      catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
       }
    }
