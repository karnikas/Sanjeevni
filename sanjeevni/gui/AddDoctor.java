    private void loadDocDetails()
    {
        try{
            doclist=EmpDao.getUnregDocs();
            Set <String> empIDset = doclist.keySet();
            for(String empID : empIDset)
            {
                comboempid.addItem(empID);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Error in DB"+ex); 
            ex.printStackTrace();
        }
    }
    
    
    private boolean validateInputs() {
      loginid=txtlogin.getText().trim();
      char[] p1=pwd1.getPassword();
      char[] p2=pwd2.getPassword();
      contactNo=txtcontact.getText().trim();
      qualification=txtqual.getText().trim();
      emailId=txtemail.getText().trim();
     special=txtspecial.getText().trim();
      if(loginid.isEmpty() || p1.length==0 || p2.length==0 || contactNo.isEmpty() || qualification.isEmpty() || emailId.isEmpty() || special.isEmpty())
      {
          return false;
      }
      pass1=new String(p1);
      pass2=new String(p2);
      return true;
    }

    private boolean validContact() {
        char[] mob = contactNo.toCharArray(); 
        for(char ch : mob){
                if(Character.isDigit(ch)==false)
                    return false; 
        }
        if(contactNo.length()==10)
            return true; 
        return false; 
    }

    private String matchpassword() {
       pass1= pass1.trim();
       pass2= pass2.trim();
       if(pass1.length()<3 || pass2.length()<3)
       {
         return "Passwords must be of atleast 3 characters in length";
       }
         if(pass1.equals(pass2)==false)
     {
       return "Passwords do not match";
     }
       return null;
    }

    private void clearALL() {
      txtcontact.setText("");     
     txtdocid.setText("");
     txtemail.setText("");
     txtlogin.setText("");
     txtqual.setText("");
    txtspecial.setText("");
    pwd1.setText("");
    pwd2.setText("");
    doclist.remove(selEmpId);
    comboempid.removeItem(selEmpId);
    if(doclist.size()==0){
          JOptionPane.showMessageDialog(null,"All doctors registered!");
          ManageDoctors manageDoc=new ManageDoctors();
          manageDoc.setVisible(true);
          this.dispose();
    }
    }
    
