    private void btnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisterActionPerformed
         if (validateInputs()==false)
         {
             JOptionPane.showMessageDialog(null, "Please enter all fields");
         }
        String validpassword=matchpassword();
        if(validpassword!= null)
        {
            JOptionPane.showMessageDialog(null, validpassword);
            return;
        }
        if(validContact()==false)
        {
            JOptionPane.showMessageDialog(null,"Invalid Mobile Number");
        return; 
        }
        
        try
        {
            DoctorsPojo doc = new DoctorsPojo();     //set all details from the frame into doc pojo 
            doc.setDoctor_id(txtdocid.getText());
            doc.setDoctor_name(selEmpName); 
            doc.setEmail_id(emailId);
            doc.setGender(combogender.getSelectedItem().toString());
            doc.setContact_no(contactNo);
            doc.setQualification(qualification);
            doc.setSpecialist(special);
            
            UserPojo2 user = new UserPojo2();    //setting details from frame into user pojo 
            user.setLogin_id(loginid);
            user.setUser_name(selEmpName);
             String encpassword= new String(PasswordEncryption.getEncryptedPassword(pass1));
            user.setPassword(encpassword);
            user.setUser_type("DOCTOR");
           
            
            boolean result1= DoctorsDao.addDoc (doc); //storing results that whether the details are added in both tables or not
            boolean result2= UserDao.addUser (user); 
             if(result1==true && result2==true)
                     {
                         JOptionPane.showMessageDialog(null, "Details added successfully in both tables");
                         clearALL();
                         return; 
                     }
             JOptionPane.showMessageDialog(null, "Registration failed");
             return;
  
        }
        catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Error in DB" +ex);
           ex.printStackTrace();
        }
    }//GEN-LAST:event_btnregisterActionPerformed

