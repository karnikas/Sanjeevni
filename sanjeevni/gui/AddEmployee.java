    private boolean validateInputs() {
        emp_name= txtempname.getText().trim();
        emp_salary= txtempsalary.getText().trim();
        if(emp_name.isEmpty() || emp_salary.isEmpty())
        {
            return false;
        }
        return true; 
    }

    private void generateNewId() {
       try{
           String empID =EmpDao.getNextEmpId();
           txtempno.setText(empID);
           txtempname.setText("");
           txtempsalary.setText("");
           jcempjob.setSelectedIndex(0);
        }
      catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
