    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        boolean isInputValid = validateInputs();
        if (!isInputValid) {
            JOptionPane.showMessageDialog(null, "Please input all the data!");
            return;
        }
        try {
            double esal = Double.parseDouble(emp_salary);              //local me value parse karwa di 
            EmpPojo emp = new EmpPojo();                                           //object of EmpPojo
            emp.setEmp_id(txtempno.getText().trim());
            emp.setEmp_name(emp_name);
            emp.setEmp_salary(esal);
            emp.setEmp_department(jcempjob.getSelectedItem().toString());
            boolean result = EmpDao.addEmp(emp);             //via pojo obejct, dao method is called and answer stored in result
            if(result == true)
            {
                JOptionPane.showMessageDialog(null, "Record addedd successfully");
          generateNewId();                               //method for auto generating empID
          return;
            }
            JOptionPane.showMessageDialog(null, "Could not add the record! Try Again"); 
        } 
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
     catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input numeric salary!"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnaddActionPerformed

