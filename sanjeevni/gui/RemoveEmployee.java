 private void loadEmpID() {
        try {
            List<String> empIDList = EmpDao.getAllemployeeId();            //Dao se saari IDs ki list will be displayed in the combo
            //isme dao wali empidList aa rahi hai 
            for (String id : empIDList) {
                jcidlist.addItem(id);
            }
}
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error In DB" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
 
  private boolean validateInputs() {
        if(jcidlist.getSelectedItem()==null)
        {
            return false;
        }
        return true; 
    }
