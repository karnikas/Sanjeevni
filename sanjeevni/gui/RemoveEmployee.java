    private void btnremoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveActionPerformed
        try {
            if (validateInputs() == false) {
                JOptionPane.showMessageDialog(null, "Please select the Employee ID");
            } 
            else
            {
                EmpPojo emp = new EmpPojo();
                UserPojo2 user= new UserPojo2(); 
                user.setLogin_id(jcidlist.getSelectedItem().toString());
                emp.setEmp_id(jcidlist.getSelectedItem().toString());
                boolean  result1 = EmpDao.removeEmp(emp);
                 boolean result2 = UserDao.removeUser(user);
                if (result1 == true && result2==true) {
                    JOptionPane.showMessageDialog(null, "Employee/user Removed");
                    jcidlist.removeItem(jcidlist.getSelectedItem());
                } 
                else {
                    JOptionPane.showMessageDialog(null, "Employee not removed");
                }
            }
        }
  catch(SQLException ex)
  {
      JOptionPane.showMessageDialog(null, "Error" + ex.getMessage(), "Error in DB ", JOptionPane.ERROR_MESSAGE);
  }
    }//GEN-LAST:event_btnremoveActionPerformed

